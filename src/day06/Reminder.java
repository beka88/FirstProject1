package day06;

public class Reminder {
    public static void main(String[] args) {



        System.out.println(5/2);
        System.out.println(5.0/2);
        System.out.println(5.0/2f);
        System.out.println(5%2);

        int minutes = 135;
        int hour = 135/60;
        int mintPart = 135%60;
        System.out.println("The minutes "+ minutes+ " is " + hour + " hours and " + mintPart + " minutes");

    }
}
