public class InitializingVariable {
    public static void main(String[] args) {
        String language = "";
        int languageCode = 1;
        if (languageCode ==1)
        {language = "Hello";
        }
        if (languageCode == 2)
        {language = "Salam";
        }
        if (languageCode == 3)
        {language = "Hola";
        }
        if (languageCode ==4)
        {language = "Privet";
        }
        if (languageCode ==5)
        {language = "Bonjour";
        }
        if (languageCode ==6)
        {language = "Merhaba";
        }
        else {language = "Unknown";}
        System.out.println(language + ",SDET");






    }
}
