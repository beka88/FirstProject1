package office_hour;

public class ForLoopPractice {
    public static void main(String[] args) {
        for(int x=23;x<=30; x++){
            System.out.println(x);
        }
        System.out.println("--------");
        for(int point=90; point>=80; point--){
            System.out.println(point);
        }
    }
}
